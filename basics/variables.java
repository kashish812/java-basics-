public class variables {
    public static void main(String[] args){
        int number = 23; // int for number no decimal
        float marks = 23.5f; // float decimal number 
        char grade = 'A'; // s char for ingle character 
        boolean isPassed = true; // boolean for true/false
        String name = "Kashish"; // string for  text
        System.out.println(name);
        System.out.println(number);
        System.out.println(grade);
        System.out.println( marks);
        // Value change 
        number = 45;
        System.out.println("updated number "+ number);
        //Real Life Example
         String city = "Delhi";
         System.out.println("city: "+ city);
         int population = 20000000;
         System.out.println("population:" + population);


    }
    
}
